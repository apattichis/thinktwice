# Combined benchmark dataset
FACTCHECK_BENCH = FACTCHECK_50 + FACTCHECK_EXTENDED


def get_dataset() -> list[dict]:
    """Return the full FactCheck-Bench (150 claims) formatted for the eval runner."""
    return [
        {
            "input": item["claim"],
            "mode": "claim",
            "gold_verdict": item["gold_verdict"],
            "gold_explanation": item["gold_explanation"],
            "domain": item["domain"],
            "difficulty": item["difficulty"],
            "id": item["id"],
        }
        for item in FACTCHECK_BENCH
    ]


def get_stats() -> dict:
    """Return dataset statistics."""
    domains = {}
    difficulties = {}
    verdicts = {}

    for item in FACTCHECK_BENCH:
        domains[item["domain"]] = domains.get(item["domain"], 0) + 1
        difficulties[item["difficulty"]] = difficulties.get(item["difficulty"], 0) + 1
        verdicts[item["gold_verdict"]] = verdicts.get(item["gold_verdict"], 0) + 1

    return {
        "total": len(FACTCHECK_BENCH),
        "by_domain": domains,
        "by_difficulty": difficulties,
        "by_verdict": verdicts,
    }
